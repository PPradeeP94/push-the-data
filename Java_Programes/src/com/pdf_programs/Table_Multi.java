package com.pdf_programs;

public class Table_Multi {
	
	public static void main(String[] args) {
		for (int i = 1; i <=5; i++) {
			for (int j = 1; j <=5; j++) {
				System.out.println(i+"*"+j+"="+(i*j));
			}
		}
		int b=5;
		for (int k = 1; k <=10; k++) {
			System.out.println(b+"*"+k+"="+b*k);
		}
	}

}
